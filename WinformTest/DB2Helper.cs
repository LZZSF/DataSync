        public static DataTable GetDataTable(string connectionString, CommandType cmdType, string cmdText, params OleDbParameter[] commandParameters)
        {
            //创建一个OleDbCommand对象
            OleDbCommand cmd = new OleDbCommand();
            //创建一个OleDbConnection对象
            OleDbConnection conn = new OleDbConnection(connectionString);

            try
            {
                //调用 PrepareCommand 方法，对 OleDbCommand 对象设置参数
                PrepareCommand(cmd, conn, null, cmdType, cmdText, commandParameters);
                //调用 OleDbCommand  的 ExecuteReader 方法
                OleDbDataAdapter adapter = new OleDbDataAdapter();
                adapter.SelectCommand = cmd;
                DataTable dt = new DataTable();

                adapter.Fill(dt);
                //清除参数
                cmd.Parameters.Clear();
                conn.Close();
                return dt;
            }
            catch (Exception e)
            {
                throw e;
            }
            finally
            {
                conn.Close();
                conn.Dispose();
            }
        }

