        /// <summary>
        /// 给定连接的数据库用假设参数执行一个sql命令（不返回数据集）
        /// </summary>
        /// <param name="cmdText">sql命令语句列表</param>
        /// <returns></returns>
        public static bool ExecuteNonQuery(List<string> cmdTexts)
        {
            bool result = false;
            using (OleDbConnection conn = new OleDbConnection(Conn))
            {
                conn.Open();
                OleDbTransaction trans = conn.BeginTransaction();
                OleDbCommand cmd;
                try
                {
                    foreach (var cmdText in cmdTexts)
                    {
                        cmd = conn.CreateCommand();
                        cmd.Transaction = trans;
                        cmd.CommandText = cmdText;
                        cmd.CommandTimeout = 0;
                        cmd.ExecuteNonQuery();
                    }

                    trans.Commit();
                    result = true;
                }
                catch (Exception ex)
                {
                    trans.Rollback();
                    throw ex;
                }
                finally
                {
                    conn.Close();
                }
                return result;
            }
        }

